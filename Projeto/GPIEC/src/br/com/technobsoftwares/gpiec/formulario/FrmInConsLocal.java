    private void btnConsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsActionPerformed
        try {
            Local_2 local = new Local_2();
            Local_2DAO dao = new Local_2DAO();
            local.setId_local(Integer.parseInt(txtCons.getText()));
            dao.pesquisar(local);
        
            txtLocal.setText(local.getNome_local());
            txaDesc.setText(local.getDesc_local());
            txtQtd.setText(String.valueOf(local.getQtd_local()));
            
            if (txtLocal.getText().equals("")) {
                txtCons.setText("");
                JOptionPane.showMessageDialog(null, "Local não cadastrado!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Insira um ID válido!");
        } 
    }//GEN-LAST:event_btnConsActionPerformed

    private void btnAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltActionPerformed
        if (!"".equals(txtLocal.getText())) {
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja fazer essa alteração?");
            if (resposta == 0 ) {
                txtLocal.setEnabled(true);
                txaDesc.setEnabled(true);
                alteracao = true;
            }
            else if (resposta == 1) {
                txtLocal.setEnabled(false);
                txaDesc.setEnabled(false);
                boolean alteracao = false;
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione um local primeiro!");
        }
    }//GEN-LAST:event_btnAltActionPerformed

    private void btnConAltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConAltActionPerformed
        if (alteracao == true) {
            if (!"".equals(txtLocal.getText())) {
                Local_2 local = new Local_2();
                local.setId_local(Integer.parseInt(txtCons.getText()));
                local.setNome_local(txtLocal.getText());
                local.setDesc_local(txaDesc.getText());

                txtLocal.setEnabled(false);
                txaDesc.setEnabled(false);
                boolean alteracao = false;
                Local_2DAO dao = new Local_2DAO();
                dao.alterar(local);
                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
                tblAncestorAdded(null);
            }else{
                JOptionPane.showMessageDialog(null, "Preencha o campo \"Local\"!");
            }       
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Realize uma alteração em um registro primeiro!");
        }       
    }//GEN-LAST:event_btnConAltActionPerformed

    private void btnExcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcActionPerformed
        if ("".equals(txtLocal.getText())) {
            JOptionPane.showMessageDialog(null, "Selecione um registro primeiro!");
            txtCons.requestFocus();
        }
        else
        {
            Local_2 local = new Local_2();
            int resposta=JOptionPane.showConfirmDialog(null, "Deseja mesmo excluir?");
            if (resposta == 0) {
                local.setId_local(Integer.parseInt(txtCons.getText()));
                Local_2DAO dao = new Local_2DAO();
                dao.deletar(local);
                JOptionPane.showMessageDialog(null, "Local removido com sucesso!");
                txtCons.setText("");
                txtLocal.setText("");
                txaDesc.setText("");
                tblAncestorAdded(null);
                txtCons.requestFocus();
            }
        }
    }//GEN-LAST:event_btnExcActionPerformed

    private void tblAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_tblAncestorAdded
        Local_2DAO dao = new Local_2DAO();
        List<Local_2> locais = dao.getLista();
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        model.setNumRows(0);
        for (Local_2 local : locais) {
            model.addRow(new Object[]{
                local.getId_local(),
                local.getNome_local(),
                local.getDesc_local(),
                local.getQtd_local()
            });
        }
    }//GEN-LAST:event_tblAncestorAdded

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        if (evt.getClickCount() ==  1) {
            Local_2 local = new Local_2();
            txtCons.setText(tbl.getValueAt(tbl.getSelectedRow(), 0).toString());
            txtLocal.setText(tbl.getValueAt(tbl.getSelectedRow(), 1).toString());
            txaDesc.setText(tbl.getValueAt(tbl.getSelectedRow(), 2).toString());
            txtQtd.setText(tbl.getValueAt(tbl.getSelectedRow(), 3).toString());
            
            Local_2DAO dao = new Local_2DAO();
            local.setId_local(Integer.parseInt(txtCons.getText()));
            dao.pesquisar(local);
        }
    }//GEN-LAST:event_tblMouseClicked

